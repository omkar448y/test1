package Launch_Browsers11;

public class method_with_returntype 

{
	public static void main(String args[]) 
	{
		
		
		
		int num=addition(10,20);
		
		System.out.println(num);
		
	}
	public static int addition(int a, int b) 
	{
		
		int sum=a+b;
		System.out.println(sum);
		
		
		return sum;
		
		
	}
	
	
	

}
