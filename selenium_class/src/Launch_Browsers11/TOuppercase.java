public class TOuppercase {
	
	
	public static void main(String args[])
	{
		
		String a2=uppercase("omkar");
		System.out.println(a2);
		
	}
	
	public static String uppercase(String s2) 
	{
		String upper=s2.toUpperCase();
		
		System.out.println();
		
		return upper;
	}
	
	

}
