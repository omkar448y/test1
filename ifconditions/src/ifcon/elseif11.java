package ifcon;

public class elseif11 {
	
	
	public static void main(String[] args) {
		
		
		int mark=33;
		if(mark>=65) {System.out.println("Distinction");}
	
		else if(mark>=55) {System.out.println("first class");}
		else if(mark>=45) {System.out.println("average");}
		else if(mark>=35) {System.out.println("fail");}
		else {System.out.println("no pass");}
		
		
		
		
	}

}
